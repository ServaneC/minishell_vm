#include "../include/minishell.h"

int			is_meta(char *str, int i)
{
	if (i == 0)
		return (1);
	if (str[i - 1] && str[i - 1] == '\\')
		return (str[i - 2] && str[i - 2] == '\\');
	return (str[i - 1] && str[i - 1] != '\\');
}